string load_direction();
void load_map(unordered_map<string, pair<string, string>> &m);

int main()
{
    unordered_map<string, pair<string, string>> m;
    load_map(m);
    string direction = load_direction();
    int n = direction.size();
    long long int steps = 1;
    for (auto it : m)
    {
        if (it.first[2] == 'A')
        {
            string cur = it.first;
            long long int ind = 0;
            bool cond = true;
            while (cond)
            {
                if (direction[ind % n] == 'R')
                {
                    cur = m[cur].second;
                }
                else
                {
                    cur = m[cur].first;
                }
                ind++;
                if (cur[2] == 'Z')
                {
                    int gcd = __gcd(steps, ind);
                    steps = (steps * ind) / gcd;

                    cond = false;
                }
            }
        }
    }
    cout << steps << endl;
    return 0;
}

