vector<pair<string, vector<int>>> get_input()
{
    vector<string> input = get_data();
    vector<pair<string, vector<int>>> res;
    int n = input.size();
    for (int i = 0; i < n; i++)
    {
        int m = input[i].size();
        int j = 0;
        while (j < m && input[i][j] != ' ')
            j++;
        string s = input[i].substr(0, j);
        j++;
        vector<int> cur;
        while (j < m)
        {
            int val = 0;
            while (j < m && input[i][j] >= '0' && input[i][j] <= '9')
            {
                val = val * 10 + input[i][j] - '0';
                j++;
            }
            cur.push_back(val);
            j++;
        }
        res.push_back({s, cur});
    }
    return res;
}

