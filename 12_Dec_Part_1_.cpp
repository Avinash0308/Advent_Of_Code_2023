int matches(pair<string, vector<int>> input)
{
    int ans = 0;
    string s = input.first;
    s = remove_dots(s);
    if (m.find({input.second, s}) != m.end())
        return m[{input.second, s}];
    if (input.second.size() == 0 && count_hash(s) == 0)
        ans = 1;
    else if (input.second.size() == 0)
        ans = 0;
    else if (s.size() == 0)
        ans = 0;
    else if (s[0] == '#')
    {
        string new_s = remove_hash(s, input.second[0]);
        if (new_s == "None")
            ans = 0;
        else
        {
            vector<int> temp = input.second;
            temp.erase(temp.begin());
            ans = matches({new_s, temp});
        }
    }
    else
    {
        string new_s = remove_hash(s, input.second[0]);
        if (new_s == "None")
            ans = 0;
        else
        {
            vector<int> temp = input.second;
            temp.erase(temp.begin());
            ans = matches({new_s, temp});
        }
        string temp_s = s.substr(1, s.size() - 1);
        ans += matches({temp_s, input.second});
    }
    m[{input.second, s}] = ans;
    return ans;
}

string remove_hash(string &s, int val)
{
    if (s.size() >= val && count_upto(s, val) == 0)
    {
        if (s.size() == val)
            return "";
        if (s[val] != '#')
            return s.substr(val + 1, s.size() - val - 1);
    }
    return "None";
}

int count_upto(string &s, int n)
{
    int ans = 0;
    for (int i = 0; i < n; i++)
    {
        if (s[i] == '.')
            ans++;
    }
    return ans;
}

int count_hash(string &s)
{
    int ans = 0;
    for (auto it : s)
    {
        if (it == '#')
            ans++;
    }
    return ans;
}

string remove_dots(string s)
{
    for (int i = 0; i < s.size(); i++)
    {
        if (s[i] != '.')
        {
            return s.substr(i, s.size() - i);
        }
    }
    return "";
}

